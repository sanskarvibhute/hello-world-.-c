#include <stdio.h>
int main()
{
    // code for performing mathematical oprations like =,-,*,/
    int a, b;
    a = 50;
    b = 5;

    printf("a=%d\n", a);
    printf("b=%d\n", b);
    printf("a+b=%d\n", a + b);
    printf("a-b=%d\n", a - b);
    printf("a/b=%d\n", a / b);
    printf("a*b=%d\n", a * b);
    return 0;
}